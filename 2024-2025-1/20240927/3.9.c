#include <stdio.h>
int main()
{
    char a, b, c;  // 定义字符变量a,b,c
    a = getchar(); // 从键盘输入一个字符，送给字符变量a
    b = getchar(); // 从键盘输入一个字符，送给字符变量b
    c = getchar(); // 从键盘输入一个字符，送给字符变量c
    putchar(a);    // 将变量a的值输出
    putchar(b);    // 将变量b的值输出
    putchar(c);    // 将变量c的值输出
    putchar('\n'); // 换行
    return 0;
}